    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        Compra as = new Compra();
        Produto prod = new Produto();
        int i = cbProdutos.getSelectedIndex();
        int itemprod = produtos.get(i).getCodigo();
        //INDICE CLIENTE
        int inde = jComboBox1.getSelectedIndex();
        int idusuario = clientes.get(inde).getId();
        
        //INSERINDO UMA COMPRA
        as.setData(txtData.getText());//PASSANDO UMA DATA
        //ItensComprados
        qtde.add(Float.parseFloat(txtQtde.getText()));//PASSANDO UM VALOR DESEJAVEL
        prod.setCodigo(itemprod);
        itensComprados.add(prod);
        as.setQuantidades(qtde); //RECEBE Qtde
        as.setItensComprados(itensComprados); //Codigo do Produto
        as.setValor(Float.parseFloat(txtQtde.getText()) * produtos.get(i).getValor()); //VALOR TOTAL

        //ARMAZENA COMPRA
        CompraDAO cdao = new CompraDAO();
        int id = cdao.adiciona(as, idusuario);
        //ARMAZENA OS ITENS COMPRADOS
        ItensCompradosDAO dao = new ItensCompradosDAO();
        as.setCodigo(id);
        dao.gravaItens(as);

        JOptionPane.showMessageDialog(null, "VocÃª acabou de fazer uma compra!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        limparTexto();
    }//GEN-LAST:event_btnInserirActionPerformed

