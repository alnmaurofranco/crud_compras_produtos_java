    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tbCompra.getModel();
        modelo.setNumRows(0);
        
        try {
            int apertei = jComboBox1.getSelectedIndex();
            
            int id = clientes.get(apertei).getId();
            CompraDAO cmpdao = new CompraDAO();
            compras = cmpdao.getListar(id);
            
            for (Compra p: compras)
            {
                modelo.addRow(new Object[]{
                    p.getCodigo(),
                    p.getData(),
                    p.getValor()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! " + e.getMessage());
        }
    }
    
