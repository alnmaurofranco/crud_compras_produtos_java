    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        CompraDAO dao = new CompraDAO();
        Compra comp = new Compra();

        int index = tbCompra.getSelectedRow();
        int id = clientes.get(index).getId();
        comp = dao.getListar(id).get(index);

        comp.setData(txtData.getText());
        comp.setValor(Float.parseFloat(txtValor.getText()));
        //int codigo = compras.get(index).getCodigo();
        
        dao.altera(comp);

        //JOptionPane.showMessageDialog(null, "VocÃª acabou de excluir uma compra", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        preencherTabela();  
        limparTexto();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        CompraDAO dao = new CompraDAO();
        
        int index = tbCompra.getSelectedRow();
        int codigo = compras.get(index).getCodigo();
        dao.remove(codigo);

        JOptionPane.showMessageDialog(null, "VocÃª acabou de excluir uma compra", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        preencherTabela();  
        limparTexto();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        CompraDAO dao = new CompraDAO();
        Compra as = new Compra();
        
        int index = jComboBox1.getSelectedIndex();
        int id = clientes.get(index).getId();
        
        as.setData(txtData.getText());
        as.setValor(Float.parseFloat(txtValor.getText()));
        dao.adiciona(as, id);

        JOptionPane.showMessageDialog(null, "VocÃª acabou de inserir uma compra", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        limparTexto();
        preencherTabela();        
    }//GEN-LAST:event_btnInserirActionPerformed

